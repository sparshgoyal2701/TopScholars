    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        if (NameField.getText().trim().isEmpty() || FatherNameField.getText().trim().isEmpty() || MobileNumberField.getText().trim().isEmpty() || EmailField.getText().trim().isEmpty() || GenderField.getText().trim().isEmpty() || AddressField.getText().trim().isEmpty() || HQField.getText().trim().isEmpty() || PercentageField.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter all details first !");
        }
        else {
            String query = "UPDATE `faculty` SET `name`= ?,`father_name`= ?,`gender`= ?,`address`= ?,`mobile_number`= ?,`email`= ?,`dob`= ?,`highest_qualification`= ?,`percentage`= ? WHERE faculty_id = ?";
            Connection connection;
            PreparedStatement preparedStatement;
            try {
                connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/topscholar", "root", "");
                preparedStatement = connection.prepareStatement(query);
                preparedStatement.setString(1, NameField.getText().trim());
                preparedStatement.setString(2, FatherNameField.getText().trim());
                preparedStatement.setString(3, GenderField.getText().trim());
                preparedStatement.setString(4, AddressField.getText().trim());
                preparedStatement.setString(5, MobileNumberField.getText().trim());
                preparedStatement.setString(6, EmailField.getText().trim());
                preparedStatement.setString(8, HQField.getText().trim());
                preparedStatement.setInt(9, Integer.parseInt(PercentageField.getText().trim()));
                preparedStatement.setInt(10,UserID.UserID);
                DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                format.setLenient(false);
                String date = YearCB.getSelectedItem() + "-" + MonthCB.getSelectedItem() + "-" + DateCB.getSelectedItem();
                try {
                    format.parse(date);
                    preparedStatement.setString(7, date);
                } catch (ParseException e) {
                    JOptionPane.showMessageDialog(null, "Invalid Date !");
                }
                int len=0;
                len = preparedStatement.executeUpdate();
                if (len > 0) {
                    JOptionPane.showMessageDialog(null, "Details updated Successfully !");
                }
                connection.close();
                preparedStatement.close();
                dispose();
                new FacultyMenu().setVisible(true);
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_UpdateButtonActionPerformed

