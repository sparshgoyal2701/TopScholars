    public FacultyUpdateProfile() {
        initComponents();
        try{
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/topscholar","root","");
            PreparedStatement preparedStatement = connection.prepareStatement("SELECT `name`, `mobile_number`, `email`, `father_name`, `gender`, `address`, `dob`, `highest_qualification`, `percentage`  FROM `faculty` WHERE faculty_id = ?");
            preparedStatement.setInt(1,UserID.UserID);
            ResultSet rs = preparedStatement.executeQuery();
            if(rs.next()){
                NameField.setText(rs.getString("name"));
                MobileNumberField.setText(rs.getString("mobile_number"));
                EmailField.setText(rs.getString("email"));
                FatherNameField.setText(rs.getString("father_name"));
                GenderField.setText(rs.getString("gender"));
                AddressField.setText(rs.getString("address"));
                HQField.setText(rs.getString("highest_qualification"));
                PercentageField.setText(rs.getString("percentage"));
                String date = rs.getString("dob");
                int temp = 0;
                for(int i=0;i<4;i++){
                    temp = temp * 10 + Integer.parseInt(String.valueOf(date.charAt(i)));
                }
                YearCB.setSelectedItem(Integer.toString(temp));
                temp = 0;
                for(int i=5;i<7;i++){
                    temp = temp * 10 + Integer.parseInt(String.valueOf(date.charAt(i)));
                }
                MonthCB.setSelectedItem(String.format("%02d",temp));
                temp = 0;
                for(int i=8;i<10;i++){
                    temp = temp * 10 + Integer.parseInt(String.valueOf(date.charAt(i)));
                }
                DateCB.setSelectedItem(String.format("%02d",temp));
            }
            else{
                JOptionPane.showMessageDialog(null,"Error !");
            }
            rs.close();
            connection.close();
            preparedStatement.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }

